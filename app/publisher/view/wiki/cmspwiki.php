A ferramenta '''CMSPWiki'''[https://github.com/nilzao/cmspwiki], foi criada durante a '''1° Hackathon - Desafio de dados abertos''' [http://desafiodadosabertos.org/] da Maratona hacker da [[Câmara Municipal de São Paulo]].

De acordo com a Lei nº 12.527, de 18 de Novembro de 2011 - '''Lei geral de acesso à informação pública''', a Câmara Municipal de São Paulo disponibilizou acesso a arquivos no formato XML, sobre os orçamentos da câmara e dos gabinetes, votações em sessões ordinárias e extraordinárias, tramitação dos projetos, relação de funcionários.

A proposta do '''CMSPWiki''' é utilizar esses arquivos XML, ''wikificando'' os dados disponíveis, possibilitando um resumo das informações, fazendo com que se tornem mais acessíveis e compreensíveis a população em geral, que já está familiarizada com esse formato.

Para cumprimento do disposto na Lei nº 12.527, de 18 de novembro de 2011, os órgãos e entidades públicas deverão utilizar todos os meios e instrumentos legítimos de que dispuserem, sendo obrigatória a divulgação em sítios oficiais da rede mundial de computadores (internet). 
Os sítios deverão, na forma de regulamento, atender, entre outros, aos seguintes requisitos:

I - conter ferramenta de pesquisa de conteúdo que permita o acesso à informação de forma objetiva, transparente, clara e em linguagem de fácil compreensão;
 
II - possibilitar a gravação de relatórios em diversos formatos eletrônicos, inclusive abertos e não proprietários, tais como planilhas e texto, de modo a facilitar a análise das informações;

III - possibilitar o acesso automatizado por sistemas externos em formatos abertos, estruturados e legíveis por máquina;

IV - divulgar em detalhes os formatos utilizados para estruturação da informação;

V - garantir a autenticidade e a integridade das informações disponíveis para acesso;

VI - manter atualizadas as informações disponíveis para acesso;

VII - indicar local e instruções que permitam ao interessado comunicar-se, por via eletrônica ou telefônica, com o órgão ou entidade detentora do sítio; e

VIII - adotar as medidas necessárias para garantir a acessibilidade de conteúdo para pessoas com deficiência, nos termos do art. 17 da Lei no 10.098, de 19 de dezembro de 2000, e do art. 9o da Convenção sobre os Direitos das Pessoas com Deficiência, aprovada pelo Decreto Legislativo no 186, de 9 de julho de 2008.
